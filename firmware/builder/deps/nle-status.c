/* Find PID by process name via /proc */
static pid_t find_pid(const char *name) {
    char path[64], buf[256];
    int i;
    for (i = 1; i < 32768; i++) {
        snprintf(path, sizeof(path), "/proc/%d/comm", i);
        int fd = open(path, O_RDONLY);
        if (fd < 0) continue;
        ssize_t n = read(fd, buf, sizeof(buf) - 1);
        close(fd);
        if (n <= 0) continue;
        buf[n] = 0;
        if (n > 0 && buf[n-1] == '\n') buf[n-1] = 0;
        if (strcmp(buf, name) == 0) return i;
    }
    return 0;
}

/* Freeze Nest UI and take over display */
static void takeover_display(void) {
    pid_t hb = find_pid("nlheartbeatd");
    if (hb > 0) kill(hb, SIGSTOP);
    pid_t nlc = find_pid("nlclient");
    if (nlc > 0) kill(nlc, SIGSTOP);

    /* Unblank and set backlight */
    int fd = open("/sys/class/graphics/fb0/blank", O_WRONLY);
    if (fd >= 0) { write(fd, "0\n", 2); close(fd); }
    fd = open("/sys/class/backlight/3-0036/brightness", O_WRONLY);
    if (fd >= 0) { write(fd, "120\n", 4); close(fd); }
}

/* Draw a single character at (x, y) with given BGRA color and integer scale */
static void draw_char(uint8_t *fb, int x, int y, char ch, uint32_t color, int scale) {
    int idx = (unsigned char)ch;
    if (idx >= 128) idx = '?';
    const uint8_t *glyph = font8x8_basic[idx];

    int row, col, sy, sx;
    for (row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (col = 0; col < 8; col++) {
            if (bits & (1 << col)) {
                /* Fill scale x scale block */
                for (sy = 0; sy < scale; sy++) {
                    int py = y + row * scale + sy;
                    if (py < 0 || py >= HEIGHT) continue;
                    for (sx = 0; sx < scale; sx++) {
                        int px = x + col * scale + sx;
                        if (px < 0 || px >= WIDTH) continue;
                        *(uint32_t *)(fb + py * STRIDE + px * BPP) = color;
                    }
                }
            }
        }
    }
}

/* Draw a string centered horizontally at given y position */
static void draw_text_centered(uint8_t *fb, int y, const char *text,
                                uint32_t color, int scale) {
    int len = strlen(text);
    int char_w = 8 * scale;
    int total_w = len * char_w;
    int x = (WIDTH - total_w) / 2;

    int i;
    for (i = 0; i < len; i++) {
        draw_char(fb, x + i * char_w, y, text[i], color, scale);
    }
}

int main(int argc, char *argv[]) {
    if (argc < 2) {
        fprintf(stderr, "Usage: nle-status \"Line 1\" [\"Line 2\"]\n");
        return 1;
    }

    const char *line1 = argv[1];
    const char *line2 = (argc >= 3) ? argv[2] : NULL;

    /* Take over display */
    takeover_display();

    /* Allocate framebuffer */
    uint8_t *fb = malloc(IMG_SIZE);
    if (!fb) {
        fprintf(stderr, "nle-status: malloc failed\n");
        return 1;
    }

    /* Fill with dark background: BGRA #1a1a2e → B=0x2e, G=0x1a, R=0x1a, A=0x00 */
    {
        int i;
        for (i = 0; i < WIDTH * HEIGHT; i++) {
            fb[i * BPP + 0] = BG_B;
            fb[i * BPP + 1] = BG_G;
            fb[i * BPP + 2] = BG_R;
            fb[i * BPP + 3] = 0x00;
        }
    }

    /* Colors: BGRA format (XRGB8888 but byte order is B,G,R,X on little-endian ARM) */
    uint32_t white = 0x00FFFFFF;      /* B=0xFF, G=0xFF, R=0xFF */
    uint32_t gray  = 0x00999999;      /* B=0x99, G=0x99, R=0x99 */

    if (line2) {
        /* Two lines: title at 2x scale centered vertically above middle,
           subtitle at 1x scale below */
        int title_h = 8 * 2;   /* 16px */
        int sub_h   = 8 * 1;   /* 8px */
        int gap     = 12;
        int total_h = title_h + gap + sub_h;
        int y_start = (HEIGHT - total_h) / 2;

        draw_text_centered(fb, y_start, line1, white, 2);
        draw_text_centered(fb, y_start + title_h + gap, line2, gray, 1);
    } else {
        /* Single line: 2x scale, vertically centered */
        int title_h = 8 * 2;
        int y_start = (HEIGHT - title_h) / 2;
        draw_text_centered(fb, y_start, line1, white, 2);
    }

    /* Write to both framebuffer pages */
    int fb_fd = open("/dev/fb0", O_WRONLY);
    if (fb_fd < 0) {
        fprintf(stderr, "nle-status: cannot open /dev/fb0\n");
        free(fb);
        return 1;
    }

    lseek(fb_fd, 0, SEEK_SET);
    write(fb_fd, fb, IMG_SIZE);
    write(fb_fd, fb, IMG_SIZE);

    /* Tell display controller to refresh */
    struct omapfb_update_window uw;
    memset(&uw, 0, sizeof(uw));
    uw.x = 0;
    uw.y = 0;
    uw.width = WIDTH;
    uw.height = HEIGHT;
    ioctl(fb_fd, OMAPFB_UPDATE_WINDOW, &uw);

    close(fb_fd);
    free(fb);
    return 0;
}
