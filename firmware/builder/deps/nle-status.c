/*
 * nle-status: Framebuffer text renderer for Nest Gen 2 LCD
 *
 * Paints 1-2 lines of text centered on the 320x320 display.
 * Used by nle-gallery-start to show boot/download progress.
 *
 * Usage: nle-status "Line 1" ["Line 2"]
 *
 * Takes over display (SIGSTOP nlclient/nlheartbeatd, unblank, backlight),
 * paints text, writes to both fb0 buffer pages, exits immediately.
 *
 * Integer-only math. Targets kernel 2.6.37 ARMv7.
 */

